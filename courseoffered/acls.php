<h3 style="text-align:center">FAQ</h3>
<div style="text-align:left">
<h4 class="listHeading">  1. What is the difference between BLS and ACLS?</h4>
   <div class="ngElements">Basic Life Support (BLS) is the generic term for any form of CPR and is required for all registered nurses. Advanced Cardiac Life Support (ACLS) is primarily required for RNs who work in a hospital setting and care for critically ill adults.



</p>
   </div>
<h4 class="listHeading">2. What is ACLS procedure?</h4>
   <div class="ngElements">
     <p>ACLS indicates advanced cardiovascular life support; BLS, basic life support; CPR, cardiopulmonary resuscitation; ET, endotracheal; IV, intravenous; and ROSC, return of spontaneous circulation.
 

</p>
   </div>
<h4 class="listHeading">3. What is ACLS in ICU?</h4>
   <div class="ngElements">
   <p>Advanced cardiac life support, advanced cardiovascular life support (ACLS) refers to a set of clinical guidelines for the urgent and emergent treatment of life-threatening cardiovascular conditions that will cause or have caused cardiac arrest, using advanced medical procedures, medications, and techniques.

</p>
   </div>
   <h4 class="listHeading">4. What does ACLS and BLS stand for?
</h4>
   <div class="ngElements">
   <p>Advanced Cardiac Life Support (ACLS) and Basic Life Support (BLS) are techniques used to help save lives during cardiac arrest. With proper training, these techniques are effective primary responses to cardiac emergencies.


</p>
   </div>
   
      <h4 class="listHeading">5. Do I need both BLS and ACLS?
</h4>
   <div class="ngElements">
   <p>Students in ACLS courses are not required by the AHA to have a current BLS Provider card, but they are expected to be proficient in BLS skills. Training Centers may require students to have current BLS Provider card.



</p>
   </div>
   
      <h4 class="listHeading">6. Why is ACLS important?
</h4>
   <div class="ngElements">
   <p>The main aim of ACLS is to allow nurses to administer care to adults that are facing cardiac and pulmonary arrests. This course not only equips the nurse with the skills to care for such patients, but it also allows them to diagnose such problems as well as other cardiopulmonary emergencies.


</p>
   </div>
   
      <h4 class="listHeading">7. Is ACLS hard?
</h4>
   <div class="ngElements">
   <p>Aside from my own experience, I often ask nurses what is was that made ACLS so difficult. Reasons why nurses find the ACLS course difficult include: The EKG Interpretation portion of Advanced Cardiovascular Life Support. Testing for a class that is hardly utilized in a specific field.


</p>
   </div>
   
      <h4 class="listHeading">8. How effective is ACLS?
</h4>
   <div class="ngElements">
   <p>An in-hospital investigation demonstrated that cardiac arrest detected by an ACLS-trained nurse was strongly associated with a four-fold increase in survival to discharge (38% vs. 10%) than those detected by a nurse without ACLS training.



</p>
   </div>
   
      <h4 class="listHeading">9. Should I get ACLS certified?
</h4>
   <div class="ngElements">
   <p>ACLS Certification Might Be a Job Requirement<br>
Nurses and doctors working in emergency rooms, critical care units, senior citizen homes, hospitals, and home care need ACLS training. If you need ACLS certification as part of your job, then get it soon.



</p>
   </div>
   
      <h4 class="listHeading">10. Is ACLS exam open book?
</h4>
   <div class="ngElements">
   <p>Yes, there is a written test at the end of the ACLS class. It is 50 questions. You may use your book for reference, i.e. open book.



</p>
   </div>
   
      <h4 class="listHeading">11.  What is ACLS skills test like?
</h4>
   <div class="ngElements">
   <p>The Advanced Cardiac Life Support (ACLS) skills session is an optional live examination portion to ACLS certification. During the skills session test an ACLS certified examiner will guide the student through a series of life-threatening cardiopulmonary scenarios commonly referred to as megacodes.



</p>
   </div>
   
      <h4 class="listHeading">12. How long does ACLS last?
</h4>
   <div class="ngElements">
   <p>Two years<br>
ACLS is geared towards healthcare professionals who either direct or participate in the management of cardiopulmonary arrest or other cardiovascular emergencies or personnel in emergency response. Upon successful completion of the course, students receive a course completion card, valid for two years.



</p>
   </div>
   
      <h4 class="listHeading">13. Can you retake ACLS exam?
</h4>
   <div class="ngElements">
   <p>While the certification exam is not timed, it does need to be taken in one sitting. You need to achieve a passing score of 80% or better. If you don't pass on your first attempt, don't worry. We will let you know right away whether you pass or fail and you can retake the exam until you pass.


</p>
   </div>
   
      <h4 class="listHeading">13. How often does ACLS need to be renewed?
</h4>
   <div class="ngElements">
   <p>How long is ACLS good for? Your ACLS certification is valid for two years. Every two years, you will need to get your ACLS recertification to remain actively certified. You can get your ACLS renewal online through one of our recertification courses.


</p>
