<p align="justify">Basic Life Support (BLS) is a primary medical aid provided to a person in an emergency. This procedure can prove to be very helpful when it comes to saving an individual’s life. However, the objective of BLS is not to ‘treat’ the person but to buy some valuable time until the emergency medical aid arrives. BLS course trains aspirants to recognize several life-threatening emergencies quickly, give high-quality chest compressions, deliver appropriate ventilations and provide early use of an AED. This course Reflects science and education from the American Heart Association Guidelines Update for CPR and Emergency Cardiovascular Care (ECC).


