<h3 style="text-align:center">FAQ</h3>
<div style="text-align:left">
<h4 class="listHeading">  1. What is BLS stand for?</h4>
   <div class="ngElements">Basic Life Support, or BLS, generally refers to the type of care that first-responders, healthcare providers and public safety professionals provide to anyone who is experiencing cardiac arrest, respiratory distress or an obstructed airway.



</p>
   </div>
<h4 class="listHeading">2. Does <a style="color:black;text-decoration:underline" href="https://www.stpaulscoachingcentre.com/courseoffered/acls.php">ACLS</a> override BLS?</h4>
   <div class="ngElements">
     <p>Does an ACLS certification override the requirements for a BLS certification? No! These courses are distinctly different. You do not need BLS to take ACLS, however it is highly recommended.

 

</p>
   </div>
<h4 class="listHeading">3. Who can teach BLS?</h4>
   <div class="ngElements">
   <p>They become full instructors after teaching on two ERC courses. Candidates for this course may be <a style="color:black;text-decoration:underline" href="https://www.stpaulscoachingcentre.com/health-care-professionals.php">healthcare professionals</a> or lay people and must hold an ERC BLS/AED Provider certificate, and have been nominated from their provider course as having Instructor Potential (IP).

</p>
   </div>
   <h4 class="listHeading">4. What are the 4 basic life support?
</h4>
   <div class="ngElements">
   <p>Basic life support (BLS) includes recognition of signs of sudden cardiac arrest (SCA), heart attack, stroke, and foreign-body airway obstruction (FBAO); cardiopulmonary resuscitation (CPR); and defibrillation with an automated external defibrillator (AED).


</p>
   </div>
   
      <h4 class="listHeading">5. What is CPR ratio?

</h4>
   <div class="ngElements">
   <p>Give two breaths after every 30 chest compressions. If two people are performing CPR , give one to two breaths after every 15 chest compressions.



</p>
   </div>
   
      <h4 class="listHeading">6. How do I check my BLS breathing?
</h4>
   <div class="ngElements">
   <p>When assessing signs of cardiac arrest in an unresponsive patient, check for absent or abnormal breathing by watching the chest for movements for 5 to 10 seconds. Simultaneously check the carotid pulse for a minimum of 5 seconds—but no more than 10 seconds—to determine if there is a pulse present.



</p>
   </div>
   
      <h4 class="listHeading">7. Do we check for pulse in BLS?
</h4>
   <div class="ngElements">
   <p>Pulse checks in BLS are not recommended. Rescuers can look for signs of life every 2 minutes / rhythm check if AED prompts you (AED generally has a 2 minute timer between rhythm checks)


</p>
   </div>
   
      <h4 class="listHeading">8. How many questions is the basic life support exam?
</h4>
   <div class="ngElements">
   <p>This test contains a total of 35 questions. After answering each question, you will be provided with the rationale. The rationale gives details about the correct answer. This BLS practice test is designed to teach and reinforce the content found the AHA BLS provider manual.



</p>
   </div>
   
      <h4 class="listHeading">9. How long is the basic life support exam?
</h4>
   <div class="ngElements">
   <p>The online portion of HeartCode BLS can be completed in approximately 1 to 2 hours. Time to complete the hands-on skills session varies from 60 minutes to 2 hours, depending on the experience of the student.



</p>
   </div>
   
      <h4 class="listHeading">10. Is BLS test hard?
</h4>
   <div class="ngElements">
   <p>The short answer? No, the class is not generally considered difficult – however, compared to lay rescuer CPR & First Aid classes, there is a greater amount of information covered and the skill requirements are slightly more technical.



</p>
   </div>
   
      <h4 class="listHeading">11.  Is the BLS exam multiple choice?
</h4>
   <div class="ngElements">
   <p>The practice exam consists of 10 multiple-choice questions that are derived from the BLS provider handbook and adhere to the latest ILCOR and ECC guidelines.



</p>
   </div>
   
      <h4 class="listHeading">12. Does BLS have a pretest?
</h4>
   <div class="ngElements">
   <p>Taking a BLS pretest is also a great way to familiarize yourself with the format. We've all had that dreadful experience where you've studied the material inside and out, yet struggled on the actual exam because the structure of the questions threw you off.


</p>
