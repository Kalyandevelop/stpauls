<h4 class="listHeading">  1. How long is the MOH license valid?</h4>
   <div class="ngElements">
<p>The license is valid for one year, subjected to renewal within 3 months before the expiry date. How to obtain a copy of the facility license after its issuance?
</p>
   </div>
<h4 class="listHeading">2. Which country accepts MOH?</h4>
   <div class="ngElements">
     <p>Passing the MOH you are allowed to work in 5 emirates of Kuwait except Dubai and Abu Dhabi. To work in Dubai and Abu Dhabi Nurses should pass <a style="color:black;text-decoration:underline" href="dha.php "> DHA</a> (Dubai Health Authority), <a style="color:black;text-decoration:underline" href="https://stpaulscoachingcentre.com/blog/preparation-tips-for-haad-exam-all-you-need-to-know/">HAAD</a> (Health Authority of Abu Dhabi) examination respectively and get qualified.</p>
   </div>
<h4 class="listHeading">3. Can I work in Dubai with a MOH license?</h4>
   <div class="ngElements">
   <p>Any <a style="color:black;text-decoration:underline" href="../health-care-professionals.php">healthcare professional</a> with a valid & active license can transfer their license to DOH, enabling them to work in the cities Abu Dhabi and Al Ain, or MOH to work in the northern emirates such as Sharjah, Ajman, Ras Al Khaimah, Fujairah & Umm Al Quwain.</p>
   </div>
   <h4 class="listHeading">4. Can we write the MOH exam in India?</h4>
   <div class="ngElements">
   <p>MOH has exam centers in many countries including India. In India exams are conducted by <a style="color:black;text-decoration:underline" href="https://stpaulscoachingcentre.com/blog/beginners-guide-to-the-prometric-exam/">Prometric</a>.</p>
   </div>
   <h4 class="listHeading">5. What is MOH in nursing?</h4>
   <div class="ngElements">
   <p>MOH exam for <a style="color:black;text-decoration:underline" href="https://stpaulscoachingcentre.com/blog/uaes-moh-dha-and-haad-licenses/">MOH license UAE</a> was the doorway exam which every healthcare professional needs to be qualified to get the license. MOH exam for  <a style="color:black;text-decoration:underline" href="../mbbs-coaching.php">doctors</a>, <a style="color:black;text-decoration:underline" href="https://www.stpaulscoachingcentre.com/pharmacist-training.php">pharmacist</a>, <a style="color:black;text-decoration:underline" href="https://www.stpaulscoachingcentre.com/bds-exams.php">dentist</a>,<a style="color:black;text-decoration:underline" href="../exam-for-nurses.php">nurse</a>, <a style="color:black;text-decoration:underline" href="../physiotherapist-exam.php">physiotherapist</a> etc. For any medical professional who wants to Move out and continue their career in the UAE, this license exam will be helpful.
</p>
   </div>
