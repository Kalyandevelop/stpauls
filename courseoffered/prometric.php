<h3 style="color:#d7123c"><b>Saudi Prometric</b></h3>
<p>For those seeking better employment status in the Kingdom of Saudi Arabia, taking this examination is a priority requirement. This exam determines a healthcare professional's competency because working in a different country demands a different look after the needs of international clients. The Saudi Licensing Examinations are referred to as the Prometric Exam because they are administered by Prometric, the world's leading provider of comprehensive testing and assessment services. Starting August 1, 2006, the Royal Embassy of Saudi Arabia has made a passing score in the Prometric Exam a mandatory requirement for the issuance of working visas to all medical professionals, health specialists, and health-care providers.</p>

