<h3 style="text-align:center">FAQ</h3>
<div style="text-align:left">
<h4 class="listHeading">  1. How do DataFlow verify documents?</h4>
   <div class="ngElements">
<p>The DataFlow Group conducts Primary Source Verification (PSV) by directly approaching the listed education body, previous employer or professional license regulatory entity that issued the document, to confirm its authenticity.

</p>
   </div>
<h4 class="listHeading">2. How long does DataFlow take to process?</h4>
   <div class="ngElements">
     <p>The usual verification process takes from 20 to 45 days from the time the application has been submitted. However, sometimes due to records being misplaced, document insufficiency or unforeseen delays in verifying data and documents, the timeframe for processing of verification may be extended.

</p>
   </div>
<h4 class="listHeading">3. What is primary source verification by DataFlow?</h4>
   <div class="ngElements">
   <p>PSV is a highly reliable, accurate and rigorous background screening procedure used to verify personal or professional credentials directly from the original issuing source.
</p>
   </div>
   <h4 class="listHeading">4. How do you know if Dataflow is completed?
</h4>
   <div class="ngElements">
   <p>Log in with your Dataflow Case number or Reference Number which was previously sent to your registered email address, and enter your passport number. You will see the status of your case and its associated components and will be able to download a copy of the finished report if your licensing authority allows this.


</p>
   </div>
   
    <h4 class="listHeading">5. What is the next step after DataFlow verification?
</h4>
   <div class="ngElements">
   <p>After verifying your degrees, employment certificates, practice licenses, training, good standing certificates and any other documents required by DoH, we issue a DataFlow PSV report and transfer your Data to DoH's system for the licensing process to begin.


</p>
   </div>
   
    <h4 class="listHeading">4. What is issuing authority name in DataFlow?
</h4>
   <div class="ngElements">
   <p>The Issuing Authority is the name of the institution that issued your license. They may also be the ones who issued you your Good Standing Certificate (GSC). Those are two totally different documents and should not be confused.



</p>
   </div>
   
    <h4 class="listHeading">4. What is DataFlow process?
</h4>
   <div class="ngElements">
   <p>Dataflow computing is a software paradigm based on the idea of representing computations as a directed graph, where nodes are computations and data flow along the edges. Dataflow can also be called stream processing or reactive programming.


</p>
   </div>
   
   
   <h4 class="listHeading">4. What is DataFlow for doctors?
</h4>
   <div class="ngElements">
   <p>MOH Dataflow for Doctors The process through which the MOH validates credentialing information from the institution that initially conferred or issued the credential (e.g., the authenticity of educational degrees).


</p>
